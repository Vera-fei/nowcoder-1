public class _19Mirror {
    public void Mirror(TreeNode root) {
        if (root == null || (root.right == null && root.left == null))
            return;
        TreeNode temp = root.left;
        root.left = root.right;
        root.right = temp;

        Mirror(root.left);
        Mirror(root.right);
    }
}
