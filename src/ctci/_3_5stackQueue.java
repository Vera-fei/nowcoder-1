import java.util.Stack;

public class _3_5stackQueue {
    Stack<Integer> stack1 = new Stack<Integer>();
    Stack<Integer> stack2 = new Stack<Integer>();

    public void push(int node) {
        stack1.push(node);
    }

    public int pop() {
        if (stack2.size() != 0) return stack2.pop();//2中有值肯定先用这里的值pop
        while (stack1.size() != 0) {
            int data = stack1.pop();
            stack2.push(data);
        }
        return stack2.pop();
    }
}
